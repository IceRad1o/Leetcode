/*
 f(n) 就是计算以每一个节点为根节点的组合，设计到子问题计算，使用dp
 f(n) = sum(i= 0->n-1) f(i)*f(n-i-1)
 */
class Solution96 {
public:
    int numTrees(int n) {
        vector<int> dp(n + 1, 0);
        dp[0] = 1;
        dp[1] = 1;
        for (int i = 2; i <= n; i++) {
            for (int j = 0; j < i; j++) {
                dp[i] += dp[j] * dp[i - j - 1];
            }
        }
        return dp[n];
    }
};